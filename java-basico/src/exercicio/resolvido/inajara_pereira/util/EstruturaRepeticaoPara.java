package exercicio.resolvido.inajara_pereira.util;

public class EstruturaRepeticaoPara {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Executando com FOR INCREMENTO");
        }

        for (int i = 5; i > 0; i--) {
            System.out.println("Executando com FOR DECREMENTO");
        }

    }
}
